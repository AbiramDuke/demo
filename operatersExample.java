public class operatersExample
{
	public static void main(String args[])
	{
		int x=6;
		int y=4;
		System.out.println("x + y = "+x+y);
		System.out.println("x - y = "+(x-y));
		System.out.println("x / y = "+(x/y));
		System.out.println("x * y = "+(x*y)+"\n");
		
		double a = 12.5;
		int b=7;
		System.out.println("a / b = "+(a/b));
	}
}