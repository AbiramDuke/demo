public class escapeCharacters
{
	public static void main(String args[])
	{
		System.out.println("Escape Characters\n_________________\n");
		System.out.println("\\b\"Backspace\""+"\t\t"+"|\t"+"\\"+"\\"+"\t"+"\"Back slash\""+"\t|");
		System.out.println("\\b\"New line\""+"\t\t"+"|\t"+'"'+"\t"+"\"Double quotes\""+"\t|");
		System.out.println("\\b\"Tab\""+"\t\t\t"+"|\t"+"'"+"\t"+"\"Single quote\""+"\t|");
		System.out.println("\\b\"Carriage return\""+"\t"+"|");
	}
}